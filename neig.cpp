#include "neig.h"

CTBNeighbor::CTBNeighbor(void)
{
}

CTBNeighbor::CTBNeighbor(std::string* neighbor)
{
	for (int i = 0; i < 8; i++)
	{
		mNeighbor[i] = neighbor[i];
	}
}

CTBNeighbor::~CTBNeighbor(void)
{
}

std::string CTBNeighbor::GetNeighbor(int i)
{
	return mNeighbor[i];
}

void CTBNeighbor::SetNeighbor(int i, std::string name)
{
	mNeighbor[i] = name;
}
